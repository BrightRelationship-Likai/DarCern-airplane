/**
  * @brief  CeTft320Nt绘制图片
  * @param  x:图片的起始点x坐标
  * @param  y:图片的起始点y坐标
  * @param  colorBuf:图片数据
  * @param  bufSizeWidth:图片宽
  * @param  bufSizeHight:图片高
  * @return None
  */
void ceTft320Nt_drawData(uint16 x, uint16 y, const uint8* colorBuf, uint16 bufSizeWidth, uint16 bufSizeHight)
{
#ifdef CE_TFT320_NT_CAN_SET_REGIION
    uint32 i;
    uint32 lgh = bufSizeHight * bufSizeWidth * 2;
    ceTft320Nt_setRegion(x, y, x + bufSizeWidth - 1, y + bufSizeHight - 1);
    for(i = 0; i < lgh; i += 2)
    {
        CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, (colorBuf[i]) | (uint16)(colorBuf[i + 1] << 8));
    }
#else
    int i, j;
    ceTft320Nt_setRegion(x, y, x + bufSizeWidth - 1, y + bufSizeHight - 1);
    for(j = 0; j < bufSizeHight; j++)
    {
        for(i = 0; i < bufSizeWidth; i++)
        {
            uint16 temp = (uint16)colorBuf[(j * bufSizeWidth + i) * 2 + 1] << 8 | colorBuf[(j * bufSizeWidth + i) * 2];
            ceTft320Nt_drawPoint(x + i, y + j, temp);
        }
    }
#endif
}

