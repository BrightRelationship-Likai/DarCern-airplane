/**
  * @brief  设置绘制区域
  * @param  startX:矩形左上角x坐标
  * @param  startY:矩形左上角y坐标
  * @param  endX:矩形右下角x坐标
  * @param  endY:矩形右下角y坐标
  * @return None
  */
void ceTft320Nt_setRegion(int16 startX, int16 startY, int16 endX, int16 endY)
{
    CE_L36_WRITE_DATA(CE_TFT320_NT_CMD_ADDR, 0x2A);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, startX >> 8);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, startX & 0xFF);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, endX >> 8);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, endX & 0xFF);

    CE_L36_WRITE_DATA(CE_TFT320_NT_CMD_ADDR, 0x2B);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, startY >> 8);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, startY & 0xFF);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, endY >> 8);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, endY & 0xFF);

    CE_L36_WRITE_DATA(CE_TFT320_NT_CMD_ADDR, 0x2C);
}

/**
  * @brief  CeTft320Nt在指定位置画点
  * @param  x:设置开始显示字符串的x轴坐标
  * @param  y:设置开始显示字符串的y轴坐标
  * @param  color:要显示的数据(颜色)
  * @return None
  */
void ceTft320Nt_drawPoint(int16 x, int16 y, uint16 color)
{
    if (x < 0 || y < 0)
    {
        return;
    }
    if (x > CE_TFT320_NT_WIDTH || y > CE_TFT320_NT_HIGHT)
    {
        return;
    }
    ceTft320Nt_setRegion(x, y, x, y);
    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, color);
}

/**
  * @brief  CeTft320Nt绘制矩形
  * @param  startX:矩形左上角x坐标
  * @param  startY:矩形左上角y坐标
  * @param  endX:矩形右下角x坐标
  * @param  endY:矩形右下角y坐标
  * @param  color:要显示的数据(颜色)
  * @return None
  */
void ceTft320Nt_drawRectangle(uint16 startX, uint16 startY, uint16 endX, uint16 endY, int16 color)
{
    int i, j;
    if(startX > endX || startY > endY)
    {
        return;
    }
    if(endX >= CE_TFT320_NT_WIDTH)
    {
        endX = CE_TFT320_NT_WIDTH - 1;
    }
    if(endY > CE_TFT320_NT_HIGHT)
    {
        endY = CE_TFT320_NT_HIGHT - 1;
    }

#ifdef CE_TFT320_NT_CAN_SET_REGIION
    ceTft320Nt_setRegion(startX, startY, endX, endY);
    endX++;
    endY++;
    for(i = startY; i < endY; i++)
    {
        for(j = startX; j < endX; j++)
        {
            CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, color);
        }
    }
#else
    endX++;
    endY++;
    for(i = startY; i < endY; i++)
    {
        for(j = startX; j < endX; j++)
        {
            ceTft320Nt_drawPoint(i, j, color);
        }
    }
#endif
}

/**
  * @brief  CeTft320Nt显示字符
  * @param  x:设置开始显示字符的x轴坐标
  * @param  y:设置开始显示字符的y轴坐标
  * @param  foreColor:显示字体的前景色
  * @param  backColor:显示字体的背景色
  * @param  msg:要显示的字符串指针
  * @param showSize:显示的字体大小，可选CE_TFT320_NT_EN_SIZE_F6X8或CE_TFT320_NT_EN_SIZE_F8X16
  * @return None
  */
void ceTft320Nt_showChar(int16 x, int16 y, uint16 foreColor, uint16 backColor, const char ch, CE_TFT320_NT_EN_SIZE showSize)
{
#ifdef CE_TFT320_NT_CAN_SET_REGIION
    if(showSize == CE_TFT320_NT_EN_SIZE_F6X8)
    {
        uint16  i, j, c, index;
        c = ch - 32;
        ceTft320Nt_setRegion(x,  y, x + 6 - 1, y + 8 - 1);
        index = 0x01;
        for(i = 0; i < 8; i++)
        {
            for(j = 0; j < 6; j++)
            {
                if((CE_TFT320_NT_F6x8[c][j] & index) == index)
                {
                    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, foreColor);
                }
                else
                {
                    CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, backColor);
                }
            }
            index <<= 1;
        }
    }
    else
    {
        uint8 i, j, c, m, index;
        c = ch - 32;
        ceTft320Nt_setRegion(x,  y, x + 8 - 1, y + 16 - 1);
        for(i = 0; i < 2; i++)
        {
            index = 0x01;
            for(j = 0; j < 8; j++)
            {
                for(m = 0; m < 8; m++)
                {
                    if((CE_TFT320_NT_F8X16[c * 16 + i * 8 + m] & index) == index)
                    {
                        CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, foreColor);
                    }
                    else
                    {
                        CE_L36_WRITE_DATA(CE_TFT320_NT_DATA_ADDR, backColor);
                    }
                }
                index <<= 1;
            }
        }
    }
#else
    uint16  j, m, n, c, temp, index;
    c = ch - 32;
    if(showSize == CE_TFT320_NT_EN_SIZE_F6X8)
    {
        ceTft320Nt_setRegion(x,  y, x + 6 - 1, y + 8 - 1);
        for(j = 0; j < 6; j++)
        {
            temp = CE_TFT320_NT_F6x8[c][j];
            index = 0x01;
            for(m = 0; m < 8; m++)
            {
                if((temp & index) == index)
                {
                    ceTft320Nt_drawPoint(x + j, y + m, foreColor);
                }
                else
                {
                    ceTft320Nt_drawPoint(x + j, y + m, backColor);
                }
                index <<= 1;
            }
        }
        x += 6;
    }
    else
    {
        ceTft320Nt_setRegion(x, y, x + 8 - 1, y + 16 - 1);
        for(j = 0; j < 2; j++)
        {
            for(m = 0; m < 8; m++)
            {
                temp = CE_TFT320_NT_F8X16[c * 16 + j * 8 + m];
                index = 0x01;
                for(n = 0; n < 8; n++)
                {
                    if((temp & index) == index)
                    {
                        ceTft320Nt_drawPoint(x + m, y + n + j * 8, foreColor);
                    }
                    else
                    {
                        ceTft320Nt_drawPoint(x + m, y + n + j * 8, backColor);
                    }
                    index <<= 1;
                }
            }
        }
        x += 8;
    }
#endif
}

