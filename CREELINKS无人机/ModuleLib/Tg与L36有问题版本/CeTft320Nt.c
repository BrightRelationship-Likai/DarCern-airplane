/**
  * @brief  设置屏幕亮度
  * @param  brightness:屏亮度百分比，0－100
  * @return None
  */
void ceTft320Nt_setBrightness(uint8 brightness)
{
    uint32 duty = ((uint64)brightness * CE_TFT320_NT_PWM_CYCLE) / 10000;
    if (duty > CE_TFT320_NT_PWM_CYCLE)
    {
        duty = CE_TFT320_NT_PWM_CYCLE;
    }
    else
    {
        if (duty < CE_PWM_MIN_CYCLE_NS)
        {
            duty = CE_PWM_MIN_CYCLE_NS;
        }
    }
    ceTft320Nt_ceTft320Nt->ceL36.cePwm.dutyNs = duty;
    cePwmOp.updata(&(ceTft320Nt_ceTft320Nt->ceL36.cePwm));
}

/**
  * @brief  获取字模
  * @param  msgGBK:需要获取的字符
  * @param  outBuf:输出缓存
  * @return None
  */
void ceTft320Nt_getFontData(char* msgGBK, uint8* outBuf, CE_TFT320_NT_GBK_TYPE ceTft320NtGBKType)
{
    int i = 0;
    uint32 address = 0;
    uint32 baseAddress = 0;
    uint8  readBuf[32] = { 0 };
    uint8 j, m, index, temp;

    if(((uint8)msgGBK[i] == 0xA9) && ((uint8)msgGBK[i + 1] >= 0xA1))
    {
        address = (282 + (msgGBK[i + 1] - 0xA1)) * 32 + baseAddress;
    }
    else if(((uint8)msgGBK[i] >= 0xA4) && ((uint8)msgGBK[i] <= 0xA9) && ((uint8)msgGBK[i + 1] >= 0xA1))
    {
        address = ((msgGBK[i] - 0xA1) * 94 + (msgGBK[i + 1] - 0xA1))  * 32  + baseAddress;
    }
    else if(((uint8)msgGBK[i] >= 0xA1) && ((uint8)msgGBK[i] <= 0xA9) && ((uint8)msgGBK[i + 1] >= 0xA1))
    {
        address = ((msgGBK[i] - 0xA1) * 94 + (msgGBK[i + 1] - 0xA1)) * 32 + baseAddress;
    }
    else if(((uint8)msgGBK[i] >= 0xB0) && ((uint8)msgGBK[i] <= 0xF7) && ((uint8)msgGBK[i + 1] >= 0xA1))
    {
        address = ((msgGBK[i] - 0xB0) * 94 + (msgGBK[i + 1] - 0xA1) + 846)  * 32  + baseAddress;//啊
    }
    else
    {
        address = ((msgGBK[i] - 0xA1) * 94 + (msgGBK[i + 1] - 0xA1))  * 32  + baseAddress;
    }
    ceSpiMasterOp.resetNSSBit(&(ceTft320Nt_ceTft320Nt->ceL36.ceSpiMaster));//开始从字库里获得数据
    ceSpiMasterOp.writeReadByte(&(ceTft320Nt_ceTft320Nt->ceL36.ceSpiMaster), 0x03);//0x03指令为一般读取，还有快速读取(一次读取多个)
    ceSpiMasterOp.writeReadByte(&(ceTft320Nt_ceTft320Nt->ceL36.ceSpiMaster), (address >> 16) & 0xFF);
    ceSpiMasterOp.writeReadByte(&(ceTft320Nt_ceTft320Nt->ceL36.ceSpiMaster), (address >> 8) & 0xFF);
    ceSpiMasterOp.writeReadByte(&(ceTft320Nt_ceTft320Nt->ceL36.ceSpiMaster), (address) & 0xFF);
    for(i = 0; i < 32; i++)
    {
        readBuf[i] = ceSpiMasterOp.writeReadByte(&(ceTft320Nt_ceTft320Nt->ceL36.ceSpiMaster), 0xFF);
    }
    ceSpiMasterOp.setNSSBit(&(ceTft320Nt_ceTft320Nt->ceL36.ceSpiMaster));

    for(m = 0; m < 2; m++)
    {
        for(j = 0; j < 2; j++)
        {
            index = 0x01;
            for(i = 0; i < 8; i++)
            {
                temp = 0;
                temp |= (((readBuf[0 + j * 8 + m * 16] & index) == 0) ? 0x80 : 0x00);
                temp |= (((readBuf[1 + j * 8 + m * 16] & index) == 0) ? 0x40 : 0x00);
                temp |= (((readBuf[2 + j * 8 + m * 16] & index) == 0) ? 0x20 : 0x00);
                temp |= (((readBuf[3 + j * 8 + m * 16] & index) == 0) ? 0x10 : 0x00);
                temp |= (((readBuf[4 + j * 8 + m * 16] & index) == 0) ? 0x08 : 0x00);
                temp |= (((readBuf[5 + j * 8 + m * 16] & index) == 0) ? 0x04 : 0x00);
                temp |= (((readBuf[6 + j * 8 + m * 16] & index) == 0) ? 0x02 : 0x00);
                temp |= (((readBuf[7 + j * 8 + m * 16] & index) == 0) ? 0x01 : 0x00);
                index <<= 1;
                outBuf[i * 2 + j + m * 16] = temp;
            }
        }
    }
}

/**
  * @brief  CeTft320Nt模块初始化
  * @param  ceTft320Nt:CeTft320Nt属性对象
  * @param  ceL36:CeTft320Nt模块使用的资源号
  * @return 系统状态码
  */
CE_STATUS ceTft320Nt_initial(CeTft320Nt* ceTft320Nt, CE_RESOURCE ceL36)
{
    ceTft320Nt->ceL36.ceL36BusSpeed = CE_L36_BUS_SPEED_H;
    ceL36Op.initial(&(ceTft320Nt->ceL36));

    ceTft320Nt->ceL36.cePwm.ceResource = (CE_RESOURCE)(ceL36 + CE_RES_MARK_L36_PWM);
    ceTft320Nt->ceL36.cePwm.cycleNs = CE_TFT320_NT_PWM_CYCLE;
    ceTft320Nt->ceL36.cePwm.dutyNs = CE_TFT320_NT_PWM_CYCLE - 1;
    cePwmOp.initial(&(ceTft320Nt->ceL36.cePwm));
    cePwmOp.resetBit(&(ceTft320Nt->ceL36.cePwm));//初始化时先关闭显示，初始化完成时在打开

    ceTft320Nt->ceL36.ceSpiMaster.ceResource = (CE_RESOURCE)(ceL36 + CE_RES_MARK_L36_SPI);
    ceTft320Nt->ceL36.ceSpiMaster.ceSpiMasterSpeed = CE_SPI_MASTER_SPEED_5MBPS;
    ceSpiMasterOp.initial(&(ceTft320Nt->ceL36.ceSpiMaster));

    ceTft320Nt_ceTft320Nt = ceTft320Nt;
    ceTft320Nt_tftInitial();//Tft屏初始化
    //ceTft320Nt_fill(CE_TFT320_NT_COLOR_BLACK);
    cePwmOp.start(&(ceTft320Nt->ceL36.cePwm));

    return CE_STATUS_SUCCESS;
}

