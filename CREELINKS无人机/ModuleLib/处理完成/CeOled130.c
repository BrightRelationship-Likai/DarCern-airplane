/**
  * @brief  一般用于代码调试，以控制台的方式打印调试信息
  * @param  ceOled130:CeOled130属性对象
  * @param  msg:要显示的信息
  * @return None
  */
void ceOled130_appendString(CeOled130* ceOled130, const char* msg)
{
    uint16 msgCoung = ceStringOp.strlen(msg);
    uint8 temp;
    uint16 i, j;
    for (i = 0; i < msgCoung + 1; i++)
    {
        if (msg[i] == '\r' || msg[i] == '\n')
        {
            for (j = ceOled130->asXIndex; j < CE_OLED130_WIDTH / 6; j++)
            {
                ceOled130->asBuf[j][ceOled130->asYIndex] = ' ';
            }
            ceOled130->asXIndex = 0;
            ceOled130->asYIndex++;
            if (ceOled130->asYIndex >= CE_OLED130_HIGHT / 8)
            {
                uint16 m, n;
                for (m = 0; m < CE_OLED130_HIGHT / 8 - 1; m++)
                {
                    for (n = 0; n < CE_OLED130_WIDTH / 6; n++)
                    {
                        ceOled130->asBuf[n][m] = ceOled130->asBuf[n][m + 1];
                    }
                }
                for (n = 0; n < CE_OLED130_WIDTH / 6; n++)
                {
                    ceOled130->asBuf[n][CE_OLED130_HIGHT / 8 - 1] = ' ';
                }
                ceOled130->asYIndex--;
            }
        }
        else if (msg[i] == '\0')
        {
            break;
        }
        else
        {
            ceOled130->asBuf[ceOled130->asXIndex][ceOled130->asYIndex] = msg[i];
            ceOled130->asXIndex++;
            if (ceOled130->asXIndex >= CE_OLED130_WIDTH / 6)
            {
                ceOled130->asXIndex = 0;
                ceOled130->asYIndex++;
                if (ceOled130->asYIndex >= CE_OLED130_HIGHT / 8)
                {
                    uint16 m, n;
                    for (m = 0; m < CE_OLED130_HIGHT / 8 - 1; m++)
                    {
                        for (n = 0; n < CE_OLED130_WIDTH / 6; n++)
                        {
                            ceOled130->asBuf[n][m] = ceOled130->asBuf[n][m + 1];
                        }
                    }
                    for (n = 0; n < CE_OLED130_WIDTH / 6; n++)
                    {
                        ceOled130->asBuf[n][CE_OLED130_HIGHT / 8 - 1] = ' ';
                    }
                    ceOled130->asYIndex--;
                }
            }
        }
    }
    for (i = 0; i < CE_OLED130_HIGHT / 8; i++)
    {
        for (j = 0; j < CE_OLED130_WIDTH / 6; j++)
        {
            temp = ceOled130->asBuf[j][i];
            ceOled130_showChar(ceOled130,  j * 6, i,temp, CE_OLED130_EN_SIZE_F6X8);
        }
    }
}

