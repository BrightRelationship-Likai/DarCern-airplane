/**
  * @brief  CeOled130显示16x16的中文字体，字模cn1616可从CeCN1616.h中获取
  * @param  ceOled130:CeOled130属性对象
  * @param  x:设置开始显示字符串的x轴坐标
  * @param  y:设置开始显示字符串的y轴坐标
  * @param  foreColor:显示字体的前景色
  * @param  backColor:显示字体的背景色
  * @param  cn1616:要显示的文字模，字模cn1616可从CeCN1616.h中获取
  * @return None
  */
void ceOled130_showCN1616(CeOled130* ceOled130, uint8 x, uint8 y, const uint8* cn1616)
{
    uint8 i, j, m;
    uint8 index = 0x80;
    uint8 wd;
    for (m = 0; m < 2; m++)
    {
        ceOled130_setStartPos(ceOled130, x, y + m);
        for (i = 0; i < 2; i++)
        {
            index = 0x80;
            for (j = 0; j < 8; j++)
            {
                wd = 0;
                wd |= ((cn1616[i + 0 + m * 16] & index) == index) ? 0x01 : 0x00;
                wd |= ((cn1616[i + 2 + m * 16] & index) == index) ? 0x02 : 0x00;
                wd |= ((cn1616[i + 4 + m * 16] & index) == index) ? 0x04 : 0x00;
                wd |= ((cn1616[i + 6 + m * 16] & index) == index) ? 0x08 : 0x00;
                wd |= ((cn1616[i + 8 + m * 16] & index) == index) ? 0x10 : 0x00;
                wd |= ((cn1616[i + 10 + m * 16] & index) == index) ? 0x20 : 0x00;
                wd |= ((cn1616[i + 12 + m * 16] & index) == index) ? 0x40 : 0x00;
                wd |= ((cn1616[i + 14 + m * 16] & index) == index) ? 0x80 : 0x00;
                index >>= 1;
                ceOled130_writeDat(ceOled130, wd);
            }
        }
    }
}

