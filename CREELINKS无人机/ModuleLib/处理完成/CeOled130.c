/**
  * @brief  CeOled130显示字符
  * @param  ceOled130:CeOled130属性对象
  * @param  x:设置开始显示字符的x轴坐标
  * @param  y:设置开始显示字符的y轴坐标
  * @param  msg:要显示的字符串指针
  * @param showSize:显示的字体大小，可选CE_TFT320_EN_SIZE_F6X8或CE_TFT320_EN_SIZE_F8X16
  * @return None
  */
void ceOled130_showChar(CeOled130* ceOled130, uint8 x, uint8 y, const char ch, CE_OLED130_EN_SIZE showSize)
{
    uint8 i, c;
    if (showSize == CE_OLED130_EN_SIZE_F6X8)
    {
        c =  ch - 32;
        ceOled130_setStartPos(ceOled130, x, y);
        for (i = 0; i < 6; i++)
        {
            ceOled130_writeDat(ceOled130, CE_OLED130_F6x8[c][i]);//6 * 8 = 48bit / 8bit = 6，所以每次写1个字节，一共写6次
        }
    }
    else
    {
        c = ch - 32;
        ceOled130_setStartPos(ceOled130, x, y);
        for (i = 0; i < 8; i++)
        {
            ceOled130_writeDat(ceOled130, CE_OLED130_F8X16[c * 16 + i]);//8 * 16 = 128bit / (8bit + 8bit) = 8，所以每次写2个字节，一共写8次
        }
        ceOled130_setStartPos(ceOled130, x, y + 1);
        for (i = 0; i < 8; i++)
        {
            ceOled130_writeDat(ceOled130, CE_OLED130_F8X16[c * 16 + i + 8]);
        }
    }
}

