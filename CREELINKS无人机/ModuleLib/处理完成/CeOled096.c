/**
  * @brief CeOled096显示16x16的汉字
  * @param ceOled096:CeOled096属性对象
  * @param x:设置开始显示32位有符号的数字的x轴坐标
  * @param y:设置开始显示32位有符号的数字的y轴坐标(也就是行坐标，CeOled096可以显示4行16x16的汉字)
  * @param cn1616:要显示的汉字字模，可从"CeCN1616.h"中获取
  */
void ceOled096_showCN1616(CeOled096* ceOled096, uint8 x, uint8 y, const uint8* cn1616)
{
    int i, j, m;
    uint8 index = 0x80;
    uint8 wd;
    for (m = 0; m < 2; m++)
    {
        ceOled096_setStartPos(ceOled096, x, y + m);
        for (i = 0; i < 2; i++)
        {
            index = 0x80;
            for (j = 0; j < 8; j++)
            {
                wd = 0;
                wd |= ((cn1616[i + 0 + m * 16] & index) == index) ? 0x01 : 0x00;
                wd |= ((cn1616[i + 2 + m * 16] & index) == index) ? 0x02 : 0x00;
                wd |= ((cn1616[i + 4 + m * 16] & index) == index) ? 0x04 : 0x00;
                wd |= ((cn1616[i + 6 + m * 16] & index) == index) ? 0x08 : 0x00;
                wd |= ((cn1616[i + 8 + m * 16] & index) == index) ? 0x10 : 0x00;
                wd |= ((cn1616[i + 10 + m * 16] & index) == index) ? 0x20 : 0x00;
                wd |= ((cn1616[i + 12 + m * 16] & index) == index) ? 0x40 : 0x00;
                wd |= ((cn1616[i + 14 + m * 16] & index) == index) ? 0x80 : 0x00;
                index >>= 1;
                ceOled096_writeDat(ceOled096, wd);
            }

        }
    }
}
/**
  * @brief CeOled096显示16x16的汉字
  * @param ceOled096:CeOled096属性对象
  * @param x:设置开始显示32位有符号的数字的x轴坐标
  * @param y:设置开始显示32位有符号的数字的y轴坐标(也就是行坐标，CeOled096可以显示4行16x16的汉字)
  * @param cn1616:要显示的汉字字模，可从"CeCN1616.h"中获取
  */
void ceOled096_drawData(CeOled096* ceOled096, uint8 x, uint8 y, uint8 dataWidth, uint8 dataHight, const uint8* dataBuf)
{
    unsigned int i, j;
    for (i = 0; i < dataHight; i++)
    {
        ceOled096_setStartPos(ceOled096, x, y+i);
        for (j = 0; j < dataWidth; j++)
        {
            ceOled096_writeDat(ceOled096, dataBuf[i*dataWidth + j]);
        }
    }
}

/**
  * @brief  一般用于代码调试，以控制台的方式打印调试信息
  * @param   ceOled096:CeOled096属性对象
  * @param  msg:要显示的信息
  */
void ceOled096_appendString(CeOled096* ceOled096, const char* msg)
{
    uint16 msgCoung = ceStringOp.strlen(msg);
    uint16 i, j;
    char temp;
    for (i = 0; i < msgCoung + 1; i++)
    {
        if (msg[i] == '\r' || msg[i] == '\n')
        {
            for (j = ceOled096->asXIndex; j < CE_OLED096_WIDTH / 6; j++)
            {
                ceOled096->asBuf[j][ceOled096->asYIndex] = ' ';
            }
            ceOled096->asXIndex = 0;
            ceOled096->asYIndex++;
            if (ceOled096->asYIndex >= CE_OLED096_HIGHT / 8)
            {
                uint16 m, n;
                for (m = 0; m < CE_OLED096_HIGHT / 8 - 1; m++)
                {
                    for (n = 0; n < CE_OLED096_WIDTH / 6; n++)
                    {
                        ceOled096->asBuf[n][m] = ceOled096->asBuf[n][m + 1];
                    }
                }
                for (n = 0; n < CE_OLED096_WIDTH / 6; n++)
                {
                    ceOled096->asBuf[n][CE_OLED096_HIGHT / 8 - 1] = ' ';
                }
                ceOled096->asYIndex--;
            }
        }
        else if (msg[i] == '\0')
        {
            break;
        }
        else
        {
            ceOled096->asBuf[ceOled096->asXIndex][ceOled096->asYIndex] = msg[i];
            ceOled096->asXIndex++;
            if (ceOled096->asXIndex >= CE_OLED096_WIDTH / 6)
            {
                ceOled096->asXIndex = 0;
                ceOled096->asYIndex++;
                if (ceOled096->asYIndex >= CE_OLED096_HIGHT / 8)
                {
                    uint16 m, n;
                    for (m = 0; m < CE_OLED096_HIGHT / 8 - 1; m++)
                    {
                        for (n = 0; n < CE_OLED096_WIDTH / 6; n++)
                        {
                            ceOled096->asBuf[n][m] = ceOled096->asBuf[n][m + 1];
                        }
                    }
                    for (n = 0; n < CE_OLED096_WIDTH / 6; n++)
                    {
                        ceOled096->asBuf[n][CE_OLED096_HIGHT / 8 - 1] = ' ';
                    }
                    ceOled096->asYIndex--;
                }
            }
        }
    }
    for (i = 0; i < CE_OLED096_HIGHT / 8; i++)
    {
        for (j = 0; j < CE_OLED096_WIDTH / 6; j++)
        {
            temp = ceOled096->asBuf[j][i];
            ceOled096_showChar(ceOled096, j * 6, i, temp, CE_OLED096_EN_SIZE_F6X8);
        }
    }
}
